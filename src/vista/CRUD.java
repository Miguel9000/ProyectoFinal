    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       // TODO add your handling code here:
        int confirmaBaja;
        try {
            conectarBase();
            int boletobaja=Integer.parseInt(txtEliminar.getText());

            confirmaBaja=stmt.executeUpdate("DELETE FROM boletos WHERE idBoletos = '"+boletobaja+"' ");

            if (confirmaBaja==1) {
                JOptionPane.showMessageDialog(null, "Se dio de baja el boleto de BD\n\n"+boletobaja+"\nVerifica consulta");

            } else {
                JOptionPane.showMessageDialog(null, "Error, no existe Id de ese boleto en BD\n\n"+boletobaja+"\nVerifica consulta");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al procesar baja de BD\n\n"+ex);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

