    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
       conectarBase();
        //Almacenar los valores de formulario en el objeto.
        objBoleto.setIdBoleto(Integer.parseInt(txtBoleto.getText()));
        objBoleto.setNombre(txtNombre.getText());
        objBoleto.setMatricula(Integer.parseInt(txtMatricula.getText()));
        objBoleto.setPelicula(txtPelicula.getText());
        objBoleto.setHorario(Integer.parseInt(txtHorario.getText()));
        // Variable objeto para registrar un boleto
        String altaBoleto="INSERT INTO boletos(idBoletos,nombre,matricula,pelicula,horario) VALUES ('"+objBoleto.getIdBoleto()+"','"+objBoleto.getNombre()+"','"+objBoleto.getMatricula()+"','"+objBoleto.getPelicula()+"','"+objBoleto.getHorario()+"')";
        // Estructura de tratamiento de errores en ejecución.

        try {
            stmt.executeUpdate(altaBoleto);
            JOptionPane.showMessageDialog(null,"Se registró un boleto de forma exitosa.\n\n"+objBoleto.getNombre());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error 1 de base de datos al general alta boleto\n\n Verifica\n\n"+ e);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void modBoletoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modBoletoActionPerformed
        try {
            conectarBase();
            objBoleto.setIdBoleto(Integer.parseInt(txtModIdBoleto.getText()));
            rs=stmt.executeQuery("SELECT * FROM boletos WHERE idBoletos = '"+objBoleto.getIdBoleto()+"'");

            JOptionPane.showMessageDialog(null, "Buscando boleto...");

            if (rs.next()==true) {
                
                txtModMatricula.setText(rs.getString("matricula"));
                
            } else {
                JOptionPane.showMessageDialog(null, "Error, no existe boleto alguno con ese ID \n\n"+objBoleto.getIdBoleto());
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al procesar baja de BD\n\n"+ex);
        }
    }//GEN-LAST:event_modBoletoActionPerformed

